package cc.ywxm.model.game;

public class RetentionRate
{
	private String date;
	// 注册玩家数
	private int count;
	// 次日登录玩家数
	private int count1;
	// 3日登录玩家数
	private int count3;
	// 7日登录玩家数
	private int count7;
	// 次日留存率
	private String rate1;
	// 3日留存率
	private String rate3;
	// 7日留存率
	private String rate7;

	public RetentionRate()
	{
		super();
	}

	public RetentionRate(String date, int count, String rate1, String rate3,
			String rate7)
	{
		super();
		this.date = date;
		this.count = count;
		this.rate1 = rate1;
		this.rate3 = rate3;
		this.rate7 = rate7;
	}

	public RetentionRate(String date, int count, int count1, int count3,
			int count7, String rate1, String rate3, String rate7)
	{
		super();
		this.date = date;
		this.count = count;
		this.count1 = count1;
		this.count3 = count3;
		this.count7 = count7;
		this.rate1 = rate1;
		this.rate3 = rate3;
		this.rate7 = rate7;
	}

	public String getDate()
	{
		return date;
	}

	public void setDate(String date)
	{
		this.date = date;
	}

	public int getCount()
	{
		return count;
	}

	public void setCount(int count)
	{
		this.count = count;
	}

	public String getRate1()
	{
		return rate1;
	}

	public void setRate1(String rate1)
	{
		this.rate1 = rate1;
	}

	public String getRate3()
	{
		return rate3;
	}

	public void setRate3(String rate3)
	{
		this.rate3 = rate3;
	}

	public String getRate7()
	{
		return rate7;
	}

	public void setRate7(String rate7)
	{
		this.rate7 = rate7;
	}

	public int getCount1()
	{
		return count1;
	}

	public void setCount1(int count1)
	{
		this.count1 = count1;
	}

	public int getCount3()
	{
		return count3;
	}

	public void setCount3(int count3)
	{
		this.count3 = count3;
	}

	public int getCount7()
	{
		return count7;
	}

	public void setCount7(int count7)
	{
		this.count7 = count7;
	}

}
