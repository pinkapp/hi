package cc.ywxm.model.platform;

/**
 * 玩家年龄段统计
 * 
 * @author HuangDeCai
 * @since 2012-12-19 12:55:07
 * 
 */
public class AgeGroupCount
{
	private String ageGroup;
	private int minAge;
	private int maxAge;
	private int total;

	public AgeGroupCount()
	{
		super();
	}
	
	

	public AgeGroupCount(String ageGroup, int minAge, int maxAge, int total)
	{
		super();
		this.ageGroup = ageGroup;
		this.minAge = minAge;
		this.maxAge = maxAge;
		this.total = total;
	}

	public int getTotal()
	{
		return total;
	}

	public void setTotal(int total)
	{
		this.total = total;
	}

	public int getMinAge()
	{
		return minAge;
	}

	public void setMinAge(int minAge)
	{
		this.minAge = minAge;
	}

	public int getMaxAge()
	{
		return maxAge;
	}

	public void setMaxAge(int maxAge)
	{
		this.maxAge = maxAge;
	}

	public String getAgeGroup()
	{
		return ageGroup;
	}

	public void setAgeGroup(String ageGroup)
	{
		this.ageGroup = ageGroup;
	}
}
